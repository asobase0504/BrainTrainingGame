#include "target.h"
#include "debug_proc.h"

//==========================================
//  静的メンバ変数宣言
//==========================================
int CTarget::m_nTime = 0;

//==========================================
//  コンストラクタ
//==========================================
CTarget::CTarget()
{

}

//==========================================
//  デストラクタ
//==========================================
CTarget::~CTarget()
{

}

//==========================================
//  初期化処理
//==========================================
HRESULT CTarget::Init()
{
	return CObject2D::Init();
}

//==========================================
//  終了処理
//==========================================
void CTarget::Uninit()
{
	CObject2D::Uninit();
}

//==========================================
//  更新処理
//==========================================
void CTarget::Update()
{
	if (m_nTime < m_nTiming)
	{
		SetColor(D3DXCOLOR(1.0f, 1.0f, 1.0f, 0.0f));
	}
	else
	{
		SetColor(D3DXCOLOR(1.0f, 1.0f, 1.0f, 1.0f));
	}

	CObject2D::Update();
}

//==========================================
//  描画処理
//==========================================
void CTarget::Draw()
{
	CObject2D::Draw();
}
