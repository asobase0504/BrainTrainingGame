#include "game.h"
#include "application.h"
#include "input.h"

#include "count_down.h"

#include "pause.h"
#include "timer.h"
#include "result.h"

//-----------------------------------------------------------------------------
// コンストラクタ
//-----------------------------------------------------------------------------
CGame::CGame() : m_state(COUNT_DOWN)
{
}

//-----------------------------------------------------------------------------
// デストラクタ
//-----------------------------------------------------------------------------
CGame::~CGame()
{
}

//-----------------------------------------------------------------------------
// 初期化
//-----------------------------------------------------------------------------
HRESULT CGame::Init()
{
	m_countDown = CCountDown::Create();
	return S_OK;
}

//-----------------------------------------------------------------------------
// ゲームの開始
//-----------------------------------------------------------------------------
void CGame::GameStart()
{
	m_timer = CTimer::Create(D3DXVECTOR3(100.0f, 100.0f, 0.0f), D3DXVECTOR2(30.0f, 100.0f));
	m_timer->SetTimer(60);
}

//-----------------------------------------------------------------------------
// 終了
//-----------------------------------------------------------------------------
void CGame::Uninit()
{
	CMode::Uninit();
}

//-----------------------------------------------------------------------------
// 更新
//-----------------------------------------------------------------------------
void CGame::Update()
{
	if (m_countDown != nullptr)
	{
		if (m_countDown->GetTimer() <= 0)
		{
			m_countDown->Release();
			m_countDown = nullptr;
			m_state = GAME_NOW;
			GameStart();
		}
		return;
	}

	if (CInput::GetKey()->Trigger(DIK_P))
	{
		CPause::Create();
	}

	if (m_timer->GetTimer() <= 0 && !m_timer->isStop())
	{
		m_timer->StopTimer(true);
		CResult::Create();
	}
}
